package com.DecesionMakingStatement;

public class IfElseExample {
	public static void main(String[] args) {


		int var1=30;
		int var2=20;

		// finding which variable is greater

		if(var1>var2)
		{
		// True Block
		// statement / group of statements

		System.out.println(" var1 variable value is greater than var2 variable value ");

		}
		else
		{
		// false block
		// statement /  group of statements

		System.out.println("var2 variable value is greater than var1 variable value ");
		}


		}

}
